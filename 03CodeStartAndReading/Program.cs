        // 한줄한줄씩 OS가 읽어주는 것이다.
        //         static void Main(string[] args) { 이 사이의 내용을 읽는다 }
        static void Main(string[] args) // 프로그램은 여기서부터 시작
        {
            // 한줄한줄 읽는 것은 좀 힘든 일이지만
            // 그걸 ide가 도와줌
            // 디버깅중 코드 한줄씩 실행
            // F10을 누르면 된다.
            // 의미가 있는 코드라고 가정
            Console.WriteLine("하하");
            Console.WriteLine("호호");
        } // 게임은 여기서 끝나지 않고 뺑뺑이
    }
}
